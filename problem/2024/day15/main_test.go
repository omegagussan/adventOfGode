func TestPart1Simple(t *testing.T) {
	mapz := parseMap("########\n#..O.O.#\n##@.O..#\n#...O..#\n#.#.O..#\n#...O..#\n#......#\n########\n\n<^^>>>vv<v>>v<<")
	seq := "<^^>>>vv<v>>v<<"
	expected := 2028
	result := part1(mapz, seq)
	if result != expected {
		t.Fatalf("expected %d, got %d", expected, result)
	}
}

